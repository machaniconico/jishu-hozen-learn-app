export function getPracticalCases(filter?: {
  grade?: Grade;
  category?: CategoryId;
  ability?: Ability;
}): PracticalCase[] {
  return PRACTICAL_CASES.filter((c) => {
    if (filter?.grade && c.grade !== filter.grade) return false;
    if (filter?.category && c.category !== filter.category) return false;
    if (filter?.ability && c.ability !== filter.ability) return false;
    return true;
  });
}

export function getPracticalCase(id: string): PracticalCase | undefined {
  return PRACTICAL_CASES.find((c) => c.id === id);
}
